package lab.models.lab3;

public class Separation {
    double left;
    double right;

    public Separation(double a, double b) {
        this.left = a;
        this.right = b;
    }

    public double getLeft() {
        return left;
    }

    public void setLeft(double left) {
        this.left = left;
    }

    public double getRight() {
        return right;
    }

    public void setRight(double right) {
        this.right = right;
    }
}
